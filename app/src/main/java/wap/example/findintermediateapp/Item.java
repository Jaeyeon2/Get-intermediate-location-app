package wap.example.findintermediateapp;

public class Item {

    String image;
    String allImage;
    String location;
    String address;
    String content;
    String date;
    String id;
    String x;
    String y;

    public Item(String image, String images, String location, String address, String content, String date, String id, String x, String y){
        super();
        this.image = image;
        this.allImage = images;
        this.location = location;
        this.address = address;
        this.content = content;
        this.date = date;
        this.id = id;
        this.x = x;
        this.y = y;
    }
}
